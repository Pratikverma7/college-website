			<p class="text-justify">The students of B.A. Honours (Humanities & Social Sciences) are engaged in executing 16 semester-long group projects in the current academic year (2014). The students of each group are required to have internal and external mentors. The projects take the students out of the classroom for field visits and surveys, define and understand problems through social interaction and engage with problem solving and locating innovative ways of resolving social issues. Some of them are linked with some established institutions and organizations such as Indira Gandhi National Centre for the Arts, Gandhi Smriti, Delhi Police, Delhi Jal Board, Sahitya Akademi, NTPC and others.

            These hands-on projects enable the students to connect academic pursuits with life experience. The projects have a social outreach programme and are designed to aim at making social interventions. This significant component of the course work of B.A. Honours (Humanities & Social Sciences) sensitizes the youth to different challenges faced by the nation and encourages them to participate effectively in the welfare of society, in addition to educating them about responsible citizenship.</p>
